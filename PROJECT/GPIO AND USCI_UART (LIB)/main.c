/**************************************************************************************************
 *	MAIN-ROUTINE
 *************************************************************************************************/
void main (void)
{
	/* SETUP */
	//-Clock-//
	clkSetup(sClkSetup.sDco1mhz.sSwdtOn);

	//-GPIO-//
	gpioSetup(PORT1, LED_RED, LED_RED);
	gpioIntSetup(PORT1, BUTTON, BUTTON, INT_PORT_OFF);

	//-UART-//
//	uartSetup(sUartSetup.sSmclk1Mhz.sBaud9600.sIntRxdOff);
//-------------------------------------------------------------------------------------------------
	/* PROCESS */
	while(1)
	{
		if(!(P1IN & BUTTON))
		{
			gpioHigh(PORT1, LED_RED);
		}
		else
		{
			gpioLow(PORT1, LED_RED);
		}
	}
}


