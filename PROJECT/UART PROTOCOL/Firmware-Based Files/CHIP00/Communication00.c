void
cmn00Send (void)
{
	/* Make frame */
	uint8_t arrTmpBuff[6];
	arrTmpBuff[0] = '/';
	arrTmpBuff[1] = sRegisterStatus00.bButton + 48;
	arrTmpBuff[2] = sRegisterStatus00.bIsButtonTurnOn + 48;
	arrTmpBuff[3] = '\r';
	arrTmpBuff[4] = '\n';
	arrTmpBuff[5] = '\0';

	/* Send TXD */
	txdStr(arrTmpBuff);
}
